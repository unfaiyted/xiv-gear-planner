package com.xiv.gearplanner.exceptions;

public class AriyalaParserException extends Exception {

    private static final long serialVersionUID = 8666539732191111629L;

    public AriyalaParserException() {
        super();
    }

    public AriyalaParserException(String message) {
        super(message);
    }

    public AriyalaParserException(String message, Throwable cause) {
        super(message, cause);
    }

    public AriyalaParserException(Throwable cause) {
        super(cause);
    }

    public AriyalaParserException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }


}
